    o.order_id,
    p.product_id,
    (random() * 2 + 1)::int AS quantity,
    p.unit_price,
    CASE
        WHEN random() < 0.7 THEN 0
        WHEN random() < 0.85 THEN 0.10
        WHEN random() < 0.95 THEN 0.20
        ELSE 0.50
    END AS discount
FROM orders o
JOIN products p ON p.product_id BETWEEN 1 AND 46
