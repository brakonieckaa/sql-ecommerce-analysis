    SELECT order_id
    FROM order_items
    GROUP BY order_id
    HAVING COUNT(*) < 3
)
AND NOT EXISTS (
    SELECT 1
    FROM order_items oi
    WHERE oi.order_id = o.order_id
      AND oi.product_id = p.product_id
)
ORDER BY random()
LIMIT 220;

